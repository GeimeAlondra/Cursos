# Entero
mi_entero = 10
print(mi_entero, type(mi_entero))

# Float
mi_flotante = 7.9
print(mi_flotante, type(mi_flotante))

# String
mi_cadena = 'Hola'
print(mi_cadena, type(mi_cadena))

# Bool -> True o False
mi_boolean = True
print(mi_boolean, type(mi_boolean))

# Ausencia de valor (None)
mi_none = None
print(mi_none, type(mi_none))
